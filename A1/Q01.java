class Q01Arithmatic {
    int add(int a, int b) {
        return a + b;
    }

    void sub(int a, int b) {
        System.out.println("Diff is :" + (a - b));
    }

    int mul() {
        int a = 3, b = 5;
        return a * b;
    }

    void div() {
        int a = 10, b = 2;
        System.out.println("Diff is :" + (a / b));
    }
}